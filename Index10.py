def main(s):
    """
    A string of five numbers is given. Find the sum of its numbers.
    Args:
        s(str): parameter
    Returns:
        int: answer
    """
    answer=0
    answer+=int(s[0])
    answer+=int(s[1])
    answer+=int(s[2])
    answer+=int(s[3])
    answer+=int(s[4])
    return answer